#ifndef __MMAP_H__
#define __MMAP_H__

// #include "platform.h"
// #include "bitfield.h"

/*
 * scm2010 memory map
 *
 */

#define FLASH_BASE		0x80000000
#define FLASH_SZ		(4 * 0x100000)

#define ILM_BASE		0x00000000
#define DLM_BASE		0x00200000

//======================================================================================================================
// SMU (System Management Unit)
//======================================================================================================================
#define SMU_BASE        0xf0100000

#define RESET_VECTOR(hart)    (0x200 + hart * 4)
#define TOP_CFG			0x208

#define LOWPOWER_CTRL   0x20c

#define SMU(r)          (SMU_BASE + r)

//======================================================================================================================
// SYS (System)
//======================================================================================================================
#define SYS_BASE        0xf1700000

#define SYS_CLK_CTRL	0x214
#define CORE_RESET_CTRL 0x218

#define SPI0_CFG        0x224
#define SPI1_CFG        0x240
#define UART0_CFG       0x22c
#define UART1_CFG       0x230

#define PIT0_CFG        0x248 /* Wi-Fi */
#define PIT1_CFG        0x250 /* BLE */

#define AUXADC_CFG      0x26c
#define AUXADC_CTRL     0x270
#define AUXADC_DATA(n)  (0x350 + n * 4)

#define BLE_CFG         0x280

#define USB_CFG         0x360

#define CRYPTO_CFG      0x2a4
#define IV_CFG(n)       (0x2b8 + n * 4)
#define KEY_CFG(n)      (0x2c8 + n * 4)

#define SDIO_CFG(n)		(0x2dc + n * 4)
#define SDIO_BAR(f)		(0x304 + f * 8) /* f: func */
#define SDIO_CTRL		(0x34c)

#define DEF_CLK_CFG		0x378

#define SYS(r)          (unsigned int *)(SYS_BASE + r)

//======================================================================================================================
// GPIO
//======================================================================================================================
#define GPIO_BASE_ADDR  (0xf0700000)
#define IOMUX_BASE_ADDR	(GPIO_BASE_ADDR + 0x0020)

//======================================================================================================================
// UART
//======================================================================================================================
#define UART0_BASE_ADDR	0xf0200000
#define UART1_BASE_ADDR	0xf0300000
#define UART2_BASE_ADDR	0xf1100000

//======================================================================================================================
// SPI
//======================================================================================================================
#define SPI0_BASE_ADDR  0xf0900000
#define SPI1_BASE_ADDR  0xf0a00000
#define SPI2_BASE_ADDR  0xf1500000

//======================================================================================================================
// SDIO
//======================================================================================================================
#define SDIO_BASE_ADDR  0xe0a00000

//======================================================================================================================
// USB
//======================================================================================================================
#define USB_BASE_ADDR   0xe0b00000
#define USB_DMAC_ADDR   0xe0b00200

//======================================================================================================================
// DMAC
//======================================================================================================================
#define DMAC0_BASE_ADDR 0xf0c00000
#define DMAC1_BASE_ADDR 0xf1600000

//======================================================================================================================
// RTC
//======================================================================================================================

//======================================================================================================================
// WDT
//======================================================================================================================

//======================================================================================================================
// Timer
//======================================================================================================================
#define TIMER_BASE_ADDR 0xf0400000
#define TIMER0_BASE		TIMER_BASE_ADDR
#define TIMER1_BASE		TIMER_BASE_ADDR
#define TIMER2_BASE		TIMER_BASE_ADDR
#define TIMER3_BASE		TIMER_BASE_ADDR

//======================================================================================================================
// Watchdog Timer
//======================================================================================================================
#define WDT_BASE_ADDR 	0xf1300000

//======================================================================================================================
// PWM
//======================================================================================================================
#define PMU_BASE_ADDR 	0xf1800000

//======================================================================================================================
// WLAN
//======================================================================================================================
#define MAC_BASE_START	0xe0200000
#define PHY_BASE_START  0xf0f20000

//======================================================================================================================
// BLE
//======================================================================================================================

//======================================================================================================================
// COEX
//======================================================================================================================
#define COEX_BASE_START	0xe0c00000

//======================================================================================================================
// PTA
//======================================================================================================================
#define PTA_BASE_START	0xe0d00000


//======================================================================================================================
// RF
//======================================================================================================================
#define RF_BASE_START  (0xf0e00000)

//======================================================================================================================
// RTC
//======================================================================================================================
#define RTC_BASE_ADDR	0xf0600000

//======================================================================================================================
// CRYPTO
//======================================================================================================================
#define HASH_BASE_ADDR  0xe0600000
#define PKE_BASE_ADDR   0xe0700000
#define SKE_BASE_ADDR   0xe0800000
#define TRNG_BASE_ADDR  0xe0900000

//======================================================================================================================
// EFUSE
//======================================================================================================================
#define EFUSE_BASE_ADDR 0xf0d00000

#endif
